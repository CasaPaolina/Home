// Current language
let currentGuestLang = localStorage.getItem('guestLanguage') || 'it';

// Translation function
function translateGuestPage(lang) {
    currentGuestLang = lang;
    localStorage.setItem('guestLanguage', lang);
    
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (guestTranslations[lang] && guestTranslations[lang][key]) {
            element.textContent = guestTranslations[lang][key];
        }
    });
    
    // Update flag buttons
    document.querySelectorAll('.flag-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    translateGuestPage(currentGuestLang);
    
    document.querySelectorAll('.flag-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            translateGuestPage(lang);
            // Re-fetch weather data with new language
            if (typeof fetchWeatherForecast === 'function') {
                fetchWeatherForecast();
            }
        });
    });
});
